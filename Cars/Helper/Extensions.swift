extension Data {
 static func convertJsonToData(_ dic :[String : Any]) -> Data? {
        do {
            let jsonData = try JSONSerialization.data(withJSONObject: dic, options: .prettyPrinted)
            
            return jsonData
        } catch {
            return nil
        }
    }
